"""
range() => used to generate a sequence of integers

Syntax:
range(start, stop, step)
start => starting integer from where we want to generate the sequence
stop => last (ending) integer where we want to stop generating the sequence (stop is excluded)
step => increment/decrement
"""

r1 = range(10, 15, 1) # generates 10, 11, 12, 13, 14
print(r1)

"""
To see the value generated by the range():
1. typecast the range output to list/tuple, ...
2. run a for loop on the range output
"""

l1 = list(r1)
print(l1)

for i in range(5, 10, 1):
    print(i)

