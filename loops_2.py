# for loop on a string

s1 = "Python"

for char in s1:
    print(char)

# for loop on a dictionary

student1 = {'roll':101, 'name':'John', 'percent':87.5}

print("Printing the keys:")
for i in student1:
    print(i) # 'i' stores the keys of the dict - one by one

print(student1['roll'])

print("Printing the values:")
for i in student1:
    print(student1[i])

print("Printing both key-value pairs:")
for i in student1:
    print(i, student1[i])

# for loop on dict.keys(), dict.values() and dict.items()

keys = student1.keys()
print(keys)

for key in student1.keys():
    print(key)

for value in student1.values():
    print(value)

items = student1.items()
print(items)

for item in student1.items():
    print(item, type(item))

# for loops on set

set1 = {7, 'hi', "Python", 6.4, True}

for i in set1:
    print(i)

